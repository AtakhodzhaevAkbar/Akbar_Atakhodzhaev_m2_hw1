public class DogStatistics {
    private int height;
    private int breed;
    private int length;

    public int getHeight() {
        return height;
    }

    public int getBreed() {
        return breed;
    }

    public int getLength() {
        return length;
    }

    public DogStatistics(int height, int breed, int length) {
        this.height = height;
        this.breed = breed;
        this.length = length;
    }
}
